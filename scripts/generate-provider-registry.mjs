import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const root = path.dirname(path.dirname(fileURLToPath(import.meta.url)));
const registryDir = path.join(root, "open-sse", "providers", "registry");
const target = path.join(registryDir, "index.js");
const files = fs.readdirSync(registryDir)
  .filter((name) => name.endsWith(".js") && name !== "index.js" && name !== "REGISTRY_TEMPLATE.js")
  .sort();

const imports = files.map((name, index) => `import p${index} from "./${name}";`).join("\n");
const entries = files.map((_, index) => `  p${index}`).join(",\n");
const output = `// Auto-generated by scripts/generate-provider-registry.mjs. Do not edit.\n${imports}\n\nconst providerRegistry = [\n${entries}\n];\n\nexport default providerRegistry;\n`;
if (process.argv.includes("--check")) {
  const current = fs.readFileSync(target, "utf8");
  if (current !== output) {
    console.error("Provider registry is stale. Run npm run generate:providers.");
    process.exit(1);
  }
} else {
  fs.writeFileSync(target, output);
}
