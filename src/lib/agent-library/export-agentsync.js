// @ts-check
import path from "node:path";
import fs from "node:fs/promises";
import { existsSync } from "node:fs";
import { getSkillsDir } from "./paths.js";
import { listLibrarySkills } from "./skills-store.js";
import { listMcpServers } from "./mcp-store.js";
import { copyDir } from "./link.js";

/**
 * Export library into an AgentSync-compatible `.agents/` layout for coexistence
 * with dallay/agentsync or similar tools. Does not run agentsync.
 *
 * @param {string} libraryRoot
 * @param {string} destDir  e.g. /path/to/project/.agents
 */
export async function exportAgentSyncLayout(libraryRoot, destDir) {
  const agentsDir = path.resolve(destDir);
  const skillsOut = path.join(agentsDir, "skills");
  await fs.mkdir(skillsOut, { recursive: true });

  const skills = await listLibrarySkills(libraryRoot);
  // Marker so we know this export tree is ours
  await fs.writeFile(
    path.join(agentsDir, ".switchboard-export.json"),
    JSON.stringify({ managedBy: "switchboard", at: new Date().toISOString() }, null, 2),
    "utf-8"
  );

  for (const skill of skills) {
    const src = path.join(getSkillsDir(libraryRoot), skill.id);
    // Namespace export skill dirs as sb-* for coexistence with AgentSync
    const outName = skill.id.startsWith("sb-") ? skill.id : `sb-${skill.id}`;
    const dest = path.join(skillsOut, outName);
    if (existsSync(dest)) {
      // Only replace if empty of foreign content or we created it
      const marker = path.join(dest, ".switchboard-managed.json");
      if (!existsSync(marker)) {
        // Skip user-owned folder with same name
        continue;
      }
      await fs.rm(dest, { recursive: true, force: true });
    }
    await copyDir(src, dest, libraryRoot);
    await fs.writeFile(
      path.join(dest, ".switchboard-managed.json"),
      JSON.stringify({ managedBy: "switchboard", skillId: skill.id }, null, 2),
      "utf-8"
    );
  }

  const mcpServers = await listMcpServers(libraryRoot);
  const mcpTomlLines = [
    "# Generated by Switchboard Agent Library — import-friendly AgentSync layout",
    "# Run: agentsync apply  (optional; Switchboard can also Apply directly)",
    "",
    "source_dir = \".\"",
    'default_agents = ["claude", "codex", "gemini", "cursor", "opencode"]',
    "",
    "[gitignore]",
    "enabled = true",
    'marker = "Switchboard / AI Agent Symlinks"',
    "",
    "[mcp]",
    "enabled = true",
    'merge_strategy = "merge"',
    "",
  ];

  for (const s of mcpServers.filter((x) => x.enabled !== false)) {
    const key = s.id.replace(/^sb-/, "");
    mcpTomlLines.push(`[mcp_servers.${key}]`);
    if (s.command) {
      mcpTomlLines.push(`command = ${JSON.stringify(s.command)}`);
      if (s.args?.length) {
        mcpTomlLines.push(
          `args = [${s.args.map((a) => JSON.stringify(a)).join(", ")}]`
        );
      }
    }
    if (s.url) {
      mcpTomlLines.push(`url = ${JSON.stringify(s.url)}`);
    }
    mcpTomlLines.push("");
  }

  // AgentSync uses agentsync.toml
  await fs.writeFile(
    path.join(agentsDir, "agentsync.toml"),
    mcpTomlLines.join("\n"),
    "utf-8"
  );

  // Also dump raw MCP JSON for humans
  await fs.writeFile(
    path.join(agentsDir, "mcp.switchboard.json"),
    JSON.stringify({ servers: mcpServers }, null, 2),
    "utf-8"
  );

  await fs.writeFile(
    path.join(agentsDir, "README.switchboard.md"),
    `# Switchboard Agent Library export

Exported from Switchboard Agent Library.

## Contents
- \`skills/\` — Agent Skills (SKILL.md packages)
- \`agentsync.toml\` — MCP + skeleton for [dallay/agentsync](https://github.com/dallay/agentsync)
- \`mcp.switchboard.json\` — canonical MCP list

## Coexistence
Switchboard manages agent targets with the **\`sb-\`** namespace (skills dirs and MCP keys).
AgentSync/skillbook can manage other paths. Prefer not to point both tools at the same
un-namespaced skill folder.

## Apply without AgentSync
Use **Dashboard → Agent library → Apply sync** instead.
`,
    "utf-8"
  );

  return {
    ok: true,
    path: agentsDir,
    skills: skills.map((s) => s.id),
    mcpCount: mcpServers.length,
  };
}
